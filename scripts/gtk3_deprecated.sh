GtkRecentAction
GtkStyle
GtkSymbolicColor
GtkTable
GtkToggleAction
GtkToolButton
GtkUIManager
GdkCursor"
) > /tmp/gtk3.deprecated.list

for i in $files
do
   grep -f /tmp/gtk3.deprecated.list -H -n "$i"
done
