usage() {
   echo
   echo "$0 <directory> c    - only .c .h .cc .cpp .hh files"
   echo "$0 <directory> all  - all files"
   echo "$0 <file(s)>"
   echo 
   exit 1
}

if ! [ "$1" ] ; then
   usage
fi

if [ -d "$1" ] ; then
   dir="$1"
   filetype="$2"
elif [ -f "$1" ] ; then
   files="$1"
else
   usage
fi

if [ "$dir" ] ; then
   if [ "$filetype" = "c" ] ; then
      files=$(find $dir -type f -name '*.c' \
                     -o -name '*.h' \
                     -o -name '*.cc' \
                     -o -name '*.cpp' \
                     -o -name '*.hh' | \
                     grep -vE 'gtkcompat|glib-compat' | sort)
   elif [ "$filetype" = "all" ] ; then
      files=$(find $dir -type f | grep -vE 'gtkcompat|glib-compat' | sort)
   else
      usage
   fi
fi

if ! [ "$files" ] ; then
   echo "No files !!"
   exit 1
fi

(
   echo "$gtk3_deprecated_list"
   echo "expose_event
GtkAction
GtkColorSelection
GtkAlignment
GtkArrow
GtkFontSelection
GtkGradient
GtkHandleBox
GtkIconFactory
GtkIconSet
